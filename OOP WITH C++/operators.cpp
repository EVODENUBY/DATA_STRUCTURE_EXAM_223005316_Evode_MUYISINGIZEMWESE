#include <iostream>
using namespace std;
int main(){
    int a = 40, b = 17;
    cout << "\nSum of a and b: " << a + b << endl;
    cout << "Difference of a and b: " << a - b << endl;
    cout << "Product of a and b: " << a * b << endl;
    cout << "Quotient : " << a / b << endl;
    cout << "Remainder: " << a % b << endl;
    return 0;

}